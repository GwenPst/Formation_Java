package fr.dawan.variables;

public class Constantes_et_règles {

	public static void main(String[] args) {
		
		
	/* 
	 * Les constantes doivent être écrites en majuscules pour les différencier des autres variables 
		et doivent contenur le mot clef "final" (=signifie que le contenu de ma variable ne changera pas)
		*/
		
		final int VIES_MAX = 15;
		
		System.out.println(VIES_MAX);
		
	

	}

}
